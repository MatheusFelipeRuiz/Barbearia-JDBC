    private void btnLogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogarActionPerformed
        try {
            String email = txtEmail.getText().toLowerCase();
            String senha = txtPassword.getText();

            FuncionarioDTO funcionarioDTO = new FuncionarioDTO(email, senha);
            ClienteDTO clienteDTO = new ClienteDTO(email, senha);

            ClienteDAO clienteDAO = new ClienteDAO();
            FuncionarioDAO funcionarioDAO = new FuncionarioDAO();

            boolean resultado = clienteDAO.autentifacaoCliente(clienteDTO);
            boolean resultadoF = funcionarioDAO.autentifacaoFuncioario(funcionarioDTO);

            if (resultadoF) {
                TelaPrincipalFunc telaPrincipal = new TelaPrincipalFunc();
                telaPrincipal.setVisible(true);
                telaPrincipal.getTxtNomeUsuario().setText(funcionarioDAO.getNomeCompleto(email));
                tl.dispose();

            } else if (resultado) {
                TelaPrincipal telaPrincipal = new TelaPrincipal();
                telaPrincipal.setVisible(true);
                telaPrincipal.getTxtNomeUsuario().setText(clienteDAO.getNomeUsuario(email));
                tl.dispose();

            } else {
                JOptionPane.showMessageDialog(null, "E-mail ou senha errado, por favor digite novamente");
            }

        } catch (Exception exception) {
            JOptionPane.showMessageDialog(null, "Erro: " + exception);

        }
    }//GEN-LAST:event_btnLogarActionPerformed

